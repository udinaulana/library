## Структура базы данных

База данных состоит из 12 таблиц:

### Справочники:
1. **Roles** - роли пользователей для разграничения доступа
2. **Disciplines** - учебные дисциплины (с поддержкой иерархии)
3. **Difficulty_levels** - уровни сложности материалов
4. **Formats** - форматы файлов
5. **Tags** - теги для поиска и классификации
6. **Authors** - авторы и правообладатели

### Основные таблицы:
7. **Users** - пользователи системы
8. **Materials** - учебные материалы (ЯДРО СИСТЕМЫ, 100+ записей)

### Связующие таблицы:
9. **Material_authors** - связь материалов и авторов (многие ко многим)
10. **Material_tags** - связь материалов и тегов (многие ко многим)

### Таблицы статистики и взаимодействия:
11. **Material_reviews** - отзывы и оценки пользователей
12. **User_favorites** - избранное пользователей


- **Количество записей в справочниках:** 20+ в каждом

## Инструкция по развертыванию

1. Установите MySQL Server
2. Запустите SQL-скрипт `database.sql` в любом MySQL-клиенте
3. База данных будет создана автоматически со всеми таблицами и тестовыми данными


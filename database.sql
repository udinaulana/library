# Библиотека учебных материалов

## Описание предметной области

Проект представляет собой базу данных для информационной системы "Библиотека учебных материалов", предназначенной для хранения, каталогизации и организации доступа к образовательным ресурсам.

**Основные бизнес-сущности:**
- **Материалы** - учебники, статьи, видеоуроки, презентации, интерактивные курсы
- **Дисциплины/предметы** - тематические разделы (математика, физика, информатика и др.)
- **Уровни сложности** - градация материалов по сложности (для начинающих, базовый, средний, продвинутый, экспертный)
- **Форматы** - типы материалов (PDF, видео, интерактивные курсы и др.)
- **Авторы** - создатели материалов с информацией о лицензиях
- **Пользователи** - с разграничением прав доступа по ролям

**Бизнес-процессы:**
- Каталогизация новых поступлений
- Рекомендация материалов по уровню и интересам
- Управление доступом к платному контенту
- Сбор статистики использования материалов
- Формирование учебных траекторий

